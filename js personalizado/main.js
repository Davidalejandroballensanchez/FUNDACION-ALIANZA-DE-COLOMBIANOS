// FUNCIONES PARA SECCIONES AUN NO HECHAS DE LA PAGINA
function funcionAlertaSeccionBloqueada() {
    const variableSeccionBloqueada = "Disculpa, esta seccion de la pagina aun no esta terminada, por lo tanto aun no puedes acceder a ella.";
    alert(variableSeccionBloqueada);
}

// FUNCIONES PARA CUANDO ALGUIEN SE REGISTRE
function funcionAlertaRegistro() {
    const variableRegistro = "Perfecto!. Ya estas registrado, ahora te invitamos a que veas otras seccion de nuestra pagina para que nos conozcas mejor!";
    alert(variableRegistro);
}

// FUNCION PARA REDES SOCIALES QUE NO EXISTEN
function funcionAlertaRedesSocialesFalsas() {
    const variableRedesSocialesFalsas= "Disculpa, por ahora no tenemos estas redes activadas. En cuanto las activamos las tendras disponibles!";
    alert(variableRedesSocialesFalsas);
}